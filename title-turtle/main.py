import turtle  
# Importamos el módulo turtle completo.
# Ahora debemos usar turtle.nombre_funcion()

turtle.setup(400, 440, 0, 0)  
# Configura la ventana:
# 400 = ancho
# 440 = alto
# 0 = posición X en la pantalla
# 0 = posición Y en la pantalla

turtle.title("turtle is here")  
# Cambia el título de la ventana

turtle.exitonclick()  
# La ventana se cierra cuando haces clic
